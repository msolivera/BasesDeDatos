    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String menu = menuname.getText();
        int idBuscar = -1;
        if (!"".equals(id.getText())) {
            idBuscar = Integer.parseInt(id.getText());
        }
        try {

            if ("".equals(id.getText()) && "".equals(menuname.getText())) {
                AdministracionMenu.cargarMenu(jTable1);
            } else {
                AdministracionMenu.buscarMenu(idBuscar, menu, jTable1);
            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AdminMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int row = jTable1.getSelectedRow();
        int valor = (int) jTable1.getValueAt(row, 0);
        try {
            AdministracionMenu.eliminarMenu(valor, jTable1);
            JOptionPane.showMessageDialog(null, "Menu eliminado correctamente", "Eliminacion", 1);
            AdministracionMenu.cargarMenu(jTable1);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AdminMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "El men√∫ tiene al menos un metodo asociado, no se puede eliminar", "Eliminacion", 1);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

